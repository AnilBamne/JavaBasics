package Polymorphism;

public class sample3 
{

	//method overloading
	
		//2 int parameter method
		public void addition(int a, int b) 
		{
			System.out.println(a+b);
		}
		
		
		//3 int parameter
		public void addition(int a, int b, int c)
		{
			System.out.println(a+b+c);
		}
		

	
	
}
