package Loops;

public class example1_forLopp4_printOddNumFrom_1_to_9 

{

	public static void main(String[] args)
	{
				
		//start num: 1
		//end num:  9
		//incr/decr: incr by 2
		 
		//     1     11<=9    9+2=11
		for(int i=1; i<=9; i=i+2) 
		{
			System.out.println(i);   //1 3 5 7 9
		}
		
	
	}

	
}
