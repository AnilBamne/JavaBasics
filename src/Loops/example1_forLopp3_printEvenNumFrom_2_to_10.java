package Loops;

public class example1_forLopp3_printEvenNumFrom_2_to_10 

{

public static void main(String[] args) {
		
		
		//start num: 2
		//end num: 10 
		//incr/decr: incr by 2
		 
		//     2    12<=10     10+2=12
		for(int i=2; i<=10; i=i+2) 
		{
			System.out.println(i);   //2 4 6 8 10
		}
		
		
		
		
		
		
	}

	
	
}
