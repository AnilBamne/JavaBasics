package Loops;

public class example3_DoWhile1 
{

	public static void main(String[] args)
	{
		
		int i=1;     //1. start num
		
		do 
		{
			System.out.println(i);    // 1 2 3 4 5
			
			i++;      //3. incr by 1    6
		}
		while(i<=5);    //2. end num
		//   6<=5
	
		
	}

	
}
