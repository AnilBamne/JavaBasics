package Loops;

public class example2_whileLoop1
{

	public static void main(String[] args) 
	{
		
		//print num from 1 to 5
		//start num=1
		//end num= 5
		//incr/decr: incr by 1
		
		
		int i=10;    //start num
		
		//   10<=5
		while(i<=5)    //end num
		{
			System.out.println(i);     //1 2 3 4 5
			
			i++;      // 6    //incr or decr
		}
		

	
	}
}
